      <p class="">
        Narrow streets with cobblehead stones, tiny blue and white houses and on every corner you’ll find some local fishermen or charming old ladies. Ericeira is a typically Portuguese fisherman’s village on the atlantic coast, turned into a surfersvillage over the last decade. Being the first World Surf Reserve in Europe, it has a magical draw to surfers from all over the world. There is always a place to peek at the ocean and the golden sand beaches with wild cliffs are spread out all over the coast, because the city is build on a rock. It’s the perfect place to learn how to surf, go hiking, enjoy sunsets and drink the best wine.
