<article class="sun">
  <div class="wrapper">
    <section class="left-6">
      <h3 class="white">What's the weather like in Ericeira?</h3>
      <p>Sunny of course! Well, most of the time. The best time to travel to Ericeira are the months may to october. The warmest months are June, July, August and September, with an average temperature of 28 degrees. It hardly ever gets over 32 degrees, and there’s always a seabreeze, so it’s never too hot. The coolest month is January and the wettest is December.</p>
    </section>
  </div>
</article>
<article class="sea-temp">
  <div class="wrapper-11">
    <section class="right-7 shift-4">
      <h3 class="white">and the sea temperature?</h3>
      <p>With an average of 17 degrees, the sea temperature in Ericeira is pretty low compared to other southern European locations. A cold water stream runs through the Atlantic ocean and hits Portugal right at the coast of Ericeira. The ocean is warmest in August, September and October, when temperatures can reach 21 degrees. If you want to go surfing you’ll need to bring or rent a wetsuit. In autumn a 3.2 millimeter is warm enough, spring and early summer a 4.3 millimeter and in the winter months you will be kept warm only by a 5.4 millimeter suit. Some also prefer booties.</p>
    </section>
