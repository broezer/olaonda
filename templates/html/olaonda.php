<article class="world-surf-reserve">
    <header class="image-box">
      <section class="content">
        <h1 class="white">World Surf Reserve</h1>
        <h2 class="white">The first World Surf Reserve in Europe</h2>
      </section>
    </header>
    <p class="full-width">
      Ericeira is the first World Surf Reserve in Europe, since october 2011. It means that part of the coastal area in and around Ericeira is a protected area, and can never be changed in a way the waves and/or coast is affected. The World Surfing Reserves (WSR) represent a commitment to identify and preserve our planet’s most extraordinary surfing areas and the habitats around them. So what makes the Ericeira coastline so special? It’s quite unique; there is a very high density of high quality waves in only a couple of kilometers. You’ll find gentle waves that break on sand for beginners as well as extreme reefbreaks for experienced surfers. Want to learn more?

    <p>
